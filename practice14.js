// built-in String Methods in JS

let str="   This iS A stRing ";
let str1="Hello Mustqeem!";
console.log(str.toUpperCase()); //converts in 'UPPER CASE'
console.log(str.toLowerCase()); //converts in 'lower case'
console.log(str.trim());    //removes the 'whitespaces'
console.log(str.slice(6,15));   //returns 'part of string'
console.log(str.concat(str1));  //add two 'strings together'
console.log(str.replace("stRing","line"));  //replaces the strings-words
console.log(str.charAt(9));